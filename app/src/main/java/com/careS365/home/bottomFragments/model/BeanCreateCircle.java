package com.careS365.home.bottomFragments.model;

public class BeanCreateCircle {
    int cicleImg;
    String cicleName;

    public BeanCreateCircle(int cicleImg, String cicleName) {
        this.cicleImg = cicleImg;
        this.cicleName = cicleName;
    }

    public int getCicleImg() {
        return cicleImg;
    }

    public void setCicleImg(int cicleImg) {
        this.cicleImg = cicleImg;
    }

    public String getCicleName() {
        return cicleName;
    }

    public void setCicleName(String cicleName) {
        this.cicleName = cicleName;
    }


}
